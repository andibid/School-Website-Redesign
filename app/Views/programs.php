                        They should be able to:<br><br>

                        <div class="ics-indent">
                            • Demonstrate the ability to analyze problems and apply mathematical skills through intensive problem solving activities utilizing programming tools; <br>

                            • Manifest research competence in related areas of Computer Science in terms of application technology techniques, strategies, trends, and updates;<br>

                            • Design and implement computing solutions in various areas of society in accordance with the computing code of ethics;<br>

                            • Exhibit the ability to write and articulate ideas of computing concepts and effectively work in a team;<br>

                            • Apply the knowledge earned in different areas of computer science.
