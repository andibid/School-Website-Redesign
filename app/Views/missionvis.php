            <div class="about-subheading">The Michaelean Ideals</div>

            <div class="contact-box">
                <div class="contact-text">
                    <b>SERVICE TO GOD AND COUNTRY</b>
                </div>
                <div class="ics-indent">
                    - The Michaelean serves with commitment, competence, compassion, loyalty, and love.<br>

                    - He/she performs tasks and works conscientiously through its full realization and completion.<br>

                    - He/she gives his/her time and best efforts.<br>

                    - He/she sees to it that tasks promote the well-being of everyone concerned.<br>

                    - He/she can be depended upon.<br>

                    - He/she can be trusted and relied upon.<br>

                    - He/she is willing to inconvenience himself/herself for the convenience of others.
                </div>
            </div>

            <div class="contact-box">
                <div class="contact-text">
                    <b>MORAL UPRIGHTNESS</b>
                </div>
                <div class="ics-indent">
                    - The Michaelean behaves, acts and does things with the purest of intentions.<br>

                    - He/she sets a positive model of leadership and credibility in the things that he/she does.<br>

                    - He/she does not compromise his/her integrity in any way.<br>

                    - He/she is authentic, honest and fair in all his/her dealings.<br>

                    - He/she treats others with dignity and respect. He/she obeys the rules and laws of the land.
                </div>
            </div>

            <div class="contact-box">
                <div class="contact-text">
                    <b>COMMITMENT TO EXCELLENCE</b>
                </div>
                <div class="ics-indent">
                    - The Michaelean's commitment to excellence motivates and energizes him/her to realize that outstanding level of performance and a high level of productivity that he aspires.<br>

                    - He/she uses every opportunity that comes his/her way for him/her to grow and develop personally and professionally.<br>

                    - He/she gives his/her time and best efforts.<br>

                    - He/she uses his/her capacity to be the best in everything.<br>

                    - He/she rises above mediocrity and overcomes obstacles more determined and focused to get to the goals he/she has set.
                </div>
            </div>

            <div class="contact-box">
                <div class="contact-text">
                    <b>LOVE FOR HUMANITY AND INTERCULTURAL SENSITIVITY</b>
                </div>
                <div class="ics-indent">
                    - A Michaelean values the good life with others.<br>

                    - He/she is God-fearing. He/she lives a life not just for himself/herself but for other people.<br>

                    - He/she makes his/her fellowmen, his/her country and even the world his/her concern, responsibility, and accountability.<br>

                    - He/she supports environmental preservation and sustainable development.<br>

                    - He/she believes that his/her action must be responsible as he/she creates bigger ripples in the larger scheme of things.<br>

                    - He/she endeavors to understand and adapt to cultural norms wherever he/she is.<br>

                    - He/she believes that things can be done in the spirit of trust and cooperation.
                </div>
