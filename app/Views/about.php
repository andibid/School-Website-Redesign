                        We recognize that authentic learning is dependent on the learner’s self-motivation, inner sense of direction, and independent study skills to pursue life-long learning, initially for its own sake, and ultimately for the ensuing benefits thereof, not only for himself or herself, but more important, for the glocal (global and local) community of which he or she is an integral part. Knowing that authentic learning does not happen by chance, we conscientiously provide each learner with intellectual challenges and learning opportunities.<br><br>


                        Saint Michael’s College of Laguna is inspired by a genuine desire to serve mankind which will achieve a reasonable level of well-being to man by making him free from hunger and thirst. By so doing, man, liberated from his material needs is to use this very freedom to unfold to the fullest his abilities and eventually to share the fruits of his endeavor with the whole of humanity.<br><br>


                        Thus, Saint Michael’s College of Laguna provides a learning environment that will bring forth to the world individuals, who will embody the Michaelean ideal of service, moral uprightness, commitment to excellence and love for humanity.</div>
